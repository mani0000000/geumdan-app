-- 가볼만한곳 테이블
CREATE TABLE IF NOT EXISTS places (
  id            text PRIMARY KEY,
  name          text        NOT NULL,
  category      text        NOT NULL CHECK (category IN ('kids','nature','culture','travel','food')),
  area          text        NOT NULL,
  short_desc    text        NOT NULL DEFAULT '',
  description   text        NOT NULL DEFAULT '',
  address       text        NOT NULL DEFAULT '',
  thumbnail_url text,
  tags          text[]      NOT NULL DEFAULT '{}',
  distance_km   numeric,
  drive_min     integer,
  operating_hours text,
  admission_fee text,
  phone         text,
  website       text,
  published     boolean     NOT NULL DEFAULT true,
  sort_order    integer     NOT NULL DEFAULT 0,
  created_at    timestamptz NOT NULL DEFAULT now()
);

-- RLS: 공개 데이터는 누구나 읽기 가능
ALTER TABLE places ENABLE ROW LEVEL SECURITY;
CREATE POLICY "places_public_read"  ON places FOR SELECT USING (published = true);
CREATE POLICY "places_admin_all"    ON places USING (true) WITH CHECK (true);

